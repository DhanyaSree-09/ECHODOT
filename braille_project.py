# ðŸŸ© Convert dots â†’ Unicode Braille character
def dots_to_unicode(dots):
    base = 0x2800  # Unicode Braille Pattern Blank
    code = base
    for dot in dots:
        code += 1 << (dot - 1)
    return chr(code)

# ðŸŸ© Get dot pattern for a given letter
